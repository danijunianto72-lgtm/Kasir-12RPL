public void tampil_data(){
   DefaultTableModel tabel = new DefaultTableModel();
     tabel.addColumn("idMenu");
     tabel.addColumn("namaMenu");
     tabel.addColumn("hargaMenu");
     tabel.addColumn("stokMenu");
     tabel.addColumn("kategori");

//    tabel.addColumn("harga_barang");
//   tabel.addColumn("stok_barang");

   try{
   Connection conn = (Connection)kasir.koneksi.dbKonek();
   String sql = "select*from menu";
   PreparedStatement pst = conn.prepareStatement(sql);
   ResultSet rs = pst.executeQuery();
   
   while (rs.next()){
       tabel.addRow(new Object[]{
       rs.getString(1),
       rs.getString(2),
       rs.getString(3),
       rs.getString(4),
       rs.getString(5)});
       
   }
   tableMenu.setModel(tabel);
   // Custom cell renderer untuk warnai stok
// Custom cell renderer untuk kolom stok
tableMenu.getColumnModel().getColumn(3).setCellRenderer(new DefaultTableCellRenderer() {
    @Override
    public Component getTableCellRendererComponent(JTable table, Object value,
                                                   boolean isSelected, boolean hasFocus,
                                                   int row, int column) {
        Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);

        try {
            int stok = Integer.parseInt(value.toString());
            if (stok < 5) {
                c.setBackground(Color.RED);   // background merah
                c.setForeground(Color.WHITE); // teks putih biar kontras
            } else {
                // reset warna normal kalau stok aman
                if (isSelected) {
                    c.setBackground(table.getSelectionBackground());
                    c.setForeground(table.getSelectionForeground());
                } else {
                    c.setBackground(Color.WHITE);
                    c.setForeground(Color.BLACK);
                }
            }
        } catch (Exception e) {
            c.setBackground(Color.WHITE);
            c.setForeground(Color.BLACK);
        }

        return c;
    }
});


   }catch(Exception e){
               e.printStackTrace(); 

   }

   
   }
