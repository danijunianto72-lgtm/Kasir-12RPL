private void kurangiStok(String idmenu) {
    try {
          String sql = "UPDATE menu SET stok = stok - 1 WHERE idmenu = ?";
             Connection conn = (Connection)kasir.koneksi.dbKonek();

        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setInt(1, Integer.parseInt(idmenu)); // casting ke integer
        pst.executeUpdate();
        pst.close();
        conn.close();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Gagal mengurangi stok: " + e.getMessage());
    }
}

