    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
 int row = tblUser.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data dulu!");
        return;
    }

    // Ambil ID berdasar nomor urut (kita harus ambil ulang berdasarkan nama & username)
    String nama = tblUser.getValueAt(row, 1).toString();
    String username = tblUser.getValueAt(row, 2).toString();

 try (Connection conn = kasir.koneksi.dbKonek();
) {
        String sql;
        PreparedStatement pst;

        if (txtPw.getText().isEmpty()) {
            // Tanpa update password
            sql = "UPDATE pelayan SET namapelayan=?, alamat=?, role=?, telp=?, status=?, username=? WHERE username=?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, txtNamaPelayan.getText());
            pst.setString(2, txtAlamat.getText());
            pst.setString(3, cmbRole.getSelectedItem().toString());
            pst.setString(4, txtTelp.getText());
            pst.setString(5, cmbStatus.getSelectedItem().toString());
            pst.setString(6, txtUsername.getText());
            pst.setString(7, username);
        } else {
            // Update semua termasuk password
            sql = "UPDATE pelayan SET namapelayan=?, pw=?, alamat=?, role=?, telp=?, status=?, username=? WHERE username=?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, txtNamaPelayan.getText());
            pst.setString(2, txtPw.getText());
            pst.setString(3, txtAlamat.getText());
            pst.setString(4, cmbRole.getSelectedItem().toString());
            pst.setString(5, txtTelp.getText());
            pst.setString(6, cmbStatus.getSelectedItem().toString());
            pst.setString(7, txtUsername.getText());
            pst.setString(8, username);
        }

        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
        loadDataPelayan();
        clearForm();
        jButton1.setEnabled(true);
btnEdit.setEnabled(false);
btnDelete.setEnabled(false);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal edit: " + e.getMessage());
    }
    }//GEN-LAST:event_btnEditActionPerformed

