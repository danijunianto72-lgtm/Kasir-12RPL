    private void loadDataPelayan() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("No");
    model.addColumn("Nama Pelayan");
    model.addColumn("Username");
    model.addColumn("Role");
    model.addColumn("Telp");
    model.addColumn("Alamat");
    model.addColumn("Status");

try (Connection conn = kasir.koneksi.dbKonek();
) {        String sql = "SELECT idpelayan, namapelayan, username, role, telp, alamat, status FROM pelayan WHERE 1=1";

        // filter berdasarkan role
        String filterRole = cmbFilter.getSelectedItem() != null ? cmbFilter.getSelectedItem().toString() : "";
        if (!filterRole.equalsIgnoreCase("Semua") && !filterRole.isEmpty()) {
            sql += " AND role = '" + filterRole + "'";
        }

        // pencarian berdasarkan nama
        String cari = txtCari.getText().trim();
        if (!cari.isEmpty()) {
            sql += " AND LOWER(namapelayan) LIKE LOWER('%" + cari + "%')";
        }

        sql += " ORDER BY idpelayan ASC";

        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        int no = 1;
        while (rs.next()) {
            model.addRow(new Object[]{
                no++,
                rs.getString("namapelayan"),
                rs.getString("username"),
                rs.getString("role"),
                rs.getString("telp"),
                rs.getString("alamat"),
                rs.getString("status")
            });
        }

        tblUser.setModel(model);

        // persempit kolom nomor
        tblUser.getColumnModel().getColumn(0).setPreferredWidth(30);
        tblUser.getColumnModel().getColumn(0).setMaxWidth(50);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal load data: " + e.getMessage());
    }
}

