    // ================== CEK STOK RENDAH ==================
private void cekStokRendah() {
    try (Connection conn = kasir.koneksi.dbKonek()) {
        String sql = "SELECT COUNT(*) AS jumlah FROM bahan WHERE stok < 3";
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);

        if (rs.next()) {
            int jumlah = rs.getInt("jumlah");
            if (jumlah > 0) {
                lblInfo.setText(""+jumlah);
                lblInfo.setForeground(java.awt.Color.RED);
            } else {
                lblInfo.setText("0");
                lblInfo.setForeground(new java.awt.Color(0, 128, 0));
            }
        }
    } catch (Exception e) {
        lblInfo.setText("Gagal cek stok: " + e.getMessage());
    }
}

