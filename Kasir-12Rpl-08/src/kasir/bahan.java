    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
  String nama = txtNama.getText();
        String stok = txtStok.getText();
        String satuan = txtSatuan.getText();
        String harga = txtHargaBeli.getText();

        if (nama.isEmpty() || stok.isEmpty() || satuan.isEmpty() || harga.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!");
            return;
        }

        try (Connection conn = kasir.koneksi.dbKonek()) {
            String sql = "INSERT INTO bahan (namabahan, stok, satuan, hargabeli) VALUES (?, ?, ?, ?)";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, nama);
            ps.setInt(2, Integer.parseInt(stok));
            ps.setString(3, satuan);
            ps.setDouble(4, Double.parseDouble(harga));

            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
            tampilData();
            clearForm();
            cekStokRendah();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal simpan data: " + e.getMessage());
        }       
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
 int row = tblBahan.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin diedit!");
            return;
        }

        int id = Integer.parseInt(tblBahan.getValueAt(row, 0).toString());
        String nama = txtNama.getText();
        String stok = txtStok.getText();
        String satuan = txtSatuan.getText();
        String harga = txtHargaBeli.getText();

        try (Connection conn = kasir.koneksi.dbKonek()) {
            String sql = "UPDATE bahan SET namabahan=?, stok=?, satuan=?, hargabeli=? WHERE idbahan=?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, nama);
            ps.setInt(2, Integer.parseInt(stok));
            ps.setString(3, satuan);
            ps.setDouble(4, Double.parseDouble(harga));
            ps.setInt(5, id);

            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!");
            tampilData();
            cekStokRendah();

            clearForm();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal edit data: " + e.getMessage());
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
  int row = tblBahan.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus!");
            return;
        }

        int id = Integer.parseInt(tblBahan.getValueAt(row, 0).toString());

        int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try (Connection conn = kasir.koneksi.dbKonek()) {
                String sql = "DELETE FROM bahan WHERE idbahan=?";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setInt(1, id);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                tampilData();
                cekStokRendah();

                clearForm();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal hapus data: " + e.getMessage());
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnHapusActionPerformed

