    private void btnInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInfoActionPerformed
    try (Connection conn = kasir.koneksi.dbKonek()) {
        String sql = "SELECT namabahan, stok FROM bahan WHERE stok < 3 ORDER BY stok ASC";
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);

        StringBuilder pesan = new StringBuilder();
        while (rs.next()) {
            pesan.append("• ")
                 .append(rs.getString("namabahan"))
                 .append(" (stok: ")
                 .append(rs.getInt("stok"))
                 .append(")\n");
        }

        if (pesan.length() == 0) {
            JOptionPane.showMessageDialog(this, "Semua stok aman ✅", "Info Stok", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, pesan.toString(), "Bahan dengan Stok Rendah ⚠", JOptionPane.WARNING_MESSAGE);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal memeriksa stok: " + e.getMessage());
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnInfoActionPerformed

