 private void tampilData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Nama Bahan");
        model.addColumn("Stok");
        model.addColumn("Satuan");
        model.addColumn("Harga Beli");

        try (Connection conn = kasir.koneksi.dbKonek()) {
            String sql = "SELECT * FROM bahan ORDER BY idbahan ASC";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("idbahan"),
                    rs.getString("namabahan"),
                    rs.getInt("stok"),
                    rs.getString("satuan"),
                    rs.getDouble("hargabeli")
                });
            }
            tblBahan.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal tampil data: " + e.getMessage());
        }
    }
